##estrutura de control iterativa (bucle for-in range)
##
##for <variable> in range (<valor o parametro a evaluar >):
##    accion 1
##
##    ....
##
##    accion N
##la variable toma el elemento dentro del rango
##y la funcion range () -> puede tomar uno, dos o tres parametros



# ejemplo especificando un valor

for n in range (20):
    c = n**2
    print(n,c)


#espeficando dos valores:

for n in range(1,6):
    c=c**3
    print(n,c)

#especificando tres valores

for n in range (8,0, -1):
    print(n,c)
